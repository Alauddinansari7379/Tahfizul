public class SlotItem
{
    private String id, madarsaId, talibIlmId, mualimId, desc, status, manageTimeId, date, timeSlotId, startTime, endTime, slotType;
    public SlotItem() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMadarsaId() {
        return madarsaId;
    }

    public void setMadarsaId(String madarsaId) {
        this.madarsaId = madarsaId;
    }

    public String getTalibIlmId() {
        return talibIlmId;
    }

    public void setTalibIlmId(String talibIlmId) {
        this.talibIlmId = talibIlmId;
    }

    public String getMualimId() {
        return mualimId;
    }

    public void setMualimId(String mualimId) {
        this.mualimId = mualimId;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getManageTimeId() {
        return manageTimeId;
    }

    public void setManageTimeId(String manageTimeId) {
        this.manageTimeId = manageTimeId;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTimeSlotId() {
        return timeSlotId;
    }

    public void setTimeSlotId(String timeSlotId) {
        this.timeSlotId = timeSlotId;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public String getSlotType() {
        return slotType;
    }

    public void setSlotType(String slotType) {
        this.slotType = slotType;
    }
}
